		<h1 class="titre-gros" style="text-align: justify">Cheikh MC</h1>

		<h1 style="text-align: justify">Précurseur et Leader du hip hop made in Comores, il parle pour ceux qui n’arrivent pas à se faire entendre.
		</h1><br />

		<p style="text-align: justify">

			Artiste engagé, leader incontesté, le soldat qui n’hésite pas à monter sur le front en première ligne c’est lui ; 
			CHEIKH MC numéro 1 du HIP HOP made in Comores. Numero1 pour l’avoir introduit, imposé mais aussi et surtout pour 
			avoir pu maintenir son drapeau levé pendant plus d’une décennie. 
		</p>		
		
		<p style="text-align: justify">
			Engagé dans le rap dès son jeune âge, Cheikh MC crée en 1995 le 1er groupe de Rap qui a vu le jour à Moroni, 
			« LES PIRATES DU MIKA ». Après son éclatement, Cheikh ne lâche pas l’affaire. De LA BANDE A CHEIKH à la CHEIKH
			 PROD en passant par la CONFRERIE, CHEIKH MC implante et développe le Hip Hop aux Comores depuis maintenant 
			 15 ans.

		</p>
		
		<p style="text-align: justify">
			« Viré de l’école à la classe de troisième au collège, il arrive par un mélange subtil de notre langue et de celle 
			de Molière à interpeller, attirer et regrouper toute une génération, celle qui s’estime délaissée. Juste par le fait 
			de dire tout haut ce qu’ils pensent tout bas » (La Gazette des Comores – 5/8/99).

		</p>

		<p style="text-align: justify">
		
		Fort de ses tournées dans la région Océan Indien, Afrique de l’est et en France, et de ses échanges avec les rappeurs 
		de la diaspora installés en France (3ème Œil, Psy 4 de la rime, Rhoff, …). Après avoir séduit avec un album engagé et 
		novateur en 2005, il vient de redéfinir les codes du rap comorien en apportant une touche encore plus originale avec 
		ce deuxième opus.	
			
		</p>
		
		<p style="text-align: justify">
			
		Ses compositions font de lui le porte - parole d’une jeunesse délaissée. Ses rimes parlent de l’injustice, de la 
		corruption, de la politique politicienne. Il dit lui – même parler tout haut ce que les autres pensent tout bas. 
		Il croie en nos valeurs traditionnelles et dit soutenir ceux qui sont pour une réforme du grand mariage. C’est un 
		révolutionnaire comme le surnomme son public. Il prône une révolution des mentalités et des comportements. 
		</p>

		<p style="text-align: justify">
			
		Cheikh MC est fier de ses origines. Ce pays qui l’a éduqué et qui lui a donné le goût de vivre.  </p>
		
