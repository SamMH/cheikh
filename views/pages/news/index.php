« Entre l’impossible et le possible, le réalisable et l’irréalisable, 
existe une petite marge qui ne tient qu’à un seul mot : volonté. » NGO'SHAWO

<p style="text-align:justify" >
  <br />Aujourd’hui, la jeunesse des Comores se veut l’avenir de son pays. Aujourd’hui, 
elle veut bouleverser ce qui est statique, réveiller les esprits, retrouver son 
sens patriote. Ainsi naît la volonté de prendre conscience et d’agir ; ainsi le jour 
se lève, éclatant, pour mettre fin à la nuit sur les îles de la Lune. « Ngo’shawo urambuhé », 
déclare une mélodie ancienne : le jour se lève afin qu’on le voie. Ngo’shawo, déclare la jeunesse 
comorienne : tels sont le nom et la raison d’être de notre mouvement.

<p style="text-align:justify" ><br />Qui sommes-nous ? Des jeunes militants Comoriens en soif d’état de droit, d’unité et de patriotisme. Ngo’shawo, 
c’est nous, jeunesse consciente : parce que nous en sommes les premiers concernés, nous nous 
engageons résolument dans la prise de conscience et la lutte contre les problèmes sociaux, 
économiques et politiques.

<p style="text-align:justify" ><br />Où sommes-nous ? Principalement basé à Moroni, capitale des Comores, Ngo‘shawo agit dans les trois premières îles 
de l’archipel (et en vue pour Mayotte…) et dans la presque totalité des pays de la diaspora comorienne 
(France, Maroc, Sénégal, Madagascar, La Réunion et Égypte).
Le mouvement se décline en plusieurs cellules chapeautées par le siège. Ces dernières transmettent et 
représentent l’idéologie Ngo’shawo.


<p style="text-align:justify" ><br />Notre objectif pour la jeunesse : Conscientiser la jeunesse sur tous les domaines de la vie sociale. Devenir son porte parole devant 
les institutions. Militer pour ses droits. Être l’ intermédiaire entre les jeunes et l’État. Réveiller 
le sens du patriotisme comorien. « Le mouvement NGO’SHAWO cherche à éveiller la conscience collective, le sens du patriotisme et la volonté 
réelle du développement. Par le dialogue et pour l’intérêt commun de la jeunesse comorienne, le mouvement 
s’engage à contribuer à la résolution des problèmes qui touchent directement et/ou indirectement cette dernière». 
(Statuts du mouvement)

                        
</p>

<p style="text-align:justify" >
       
<br />NGO’SHAWO URAMBUHE / AU NOM DE LAREVOLUTION DES MENTALITES...

<p style="text-align:justify" ><br />Rappel de la philosophie Ngo’shawo : Etre un mouvement à but non lucratif – Demeurer indépendant 
et sans appartenance politique – Agir sur trois grands axes : UNITÉ DE LA JEUNESSE - CONSCIENTISATION 
DE LA JEUNESSE PAR LA JEUNESSE [ LA RÉVOLUTION DES MENTALITES ] - PORTE-PAROLE DE LA JEUNESSE.

<p style="text-align:justify" ><br />1 – UNITÉ DE LA JEUNESSE : Depuis sa création, Ngo’shawo a milité pour l’unité de la jeunesse dans toutes les îles de l’archipel, 
avec l’espoir d’être présent à Mayotte. Grâce à l’implication des jeunes partis étudier à l’étranger, 
des cellules ont vu le jour dans les principaux pays de la diaspora (France, Maroc, Sénégal, Egypte et Madagascar). 
Parce que les idées n’ont pas de communauté propre, de village ou de quartier dédié, Ngo’shawo se démarque du 
sectarisme et s’engage à fédérer les initiatives de tous les jeunes, où qu’ils se trouvent.

<p style="text-align:justify" ><br />Ensemble pour parler d’une seule voix : Aujourd’hui la jeunesse est confrontée à des clivages qu’elle doit faire disparaître afin d’œuvrer pour 
le bien des Comores. Premièrement, les jeunes n’étudient plus nécessairement au même endroit, même au sein 
d’une même île. Cela ne favorise pas la rencontre, l’échange d’idées et le partage des idéaux qui feront 
l’avenir du pays. Par ailleurs, le séparatisme insulaire est un réel problème à surmonter. Ngo’shawo croit 
fermement en une identité d’abord Comorienne : on est Comorien avant d’être de Ngazidja, de Ndzuani, de Mwali 
ou de Maore. Nous devons donc dépasser l’identification à notre île dite « d’origine » et nous rencontrer, 
interagir les uns avec les autres, à travers des projets qui contribueront à la conscientisation de la jeunesse.


<p style="text-align:justify" ><br />2 – CONSCIENTISER LA JEUNESSE PAR LA JEUNESSE : Grâce à l’échange et à l’interaction, nous bâtirons ensemble l’idéologie qui construira les Comores de demain. 
Grâce à notre effort commun, nous agirons pour l’épanouissement du jeune Comorien et pour sa prise de conscience.
 C’est une étape nécessaire pour combattre le pessimisme et le fatalisme ambiant. Un autre avenir est possible, 
 mais il dépend de notre volonté : c’est là le principe de la Révolution des mentalités.

<p style="text-align:justify" ><br />Des exemples d’unité : Chaque cellule, où qu’elle se trouve, peut entamer un projet et y associer les différentes cellules Ngo’shawo 
à travers le monde : cela rejoint l’objectif d’unification. Par exemple, pour un festival qui se déroulerait à 
Mohéli, les membres d’Anjouan et de Grande-Comores pourraient s’unir aux membres mohéliens et réaliser le projet. 
Celui-ci peut consister en des conférences, des forums, des émissions de radiotélévision ou des interviews, des 
colloques, des festivals, des films, etc.


<p style="text-align:justify" ><br />Les projets de Ngo’shawo peuvent s’articuler autour de causes diverses : La défense des droits de la jeunesse 
et de l’enfant – La valorisation de la culture comorienne – La condition féminine et la place de la femme aux 
Comores – La lutte contre la délinquance juvénile et ses innombrables fléaux : drogue, alcool, Sida et autres MST, 
grossesses non désirées et avortements clandestins, pédophilie et viols consentis, exploitation des enfants, éducation) 
– Les conflits de tradition : (réforme du Aanda).

<p style="text-align:justify" ><br />3 – LA VOIX DE LA JEUNESSE : Pour cette jeunesse que l’on oublie, et que l’on n’évoque qu’à des occasions précises (élections, lutte contre le Sida, 
délinquance, corruption, chômage ou sport) Ngo’shawo se veut le porte-parole devant l’Etat et les institutions. 
Nous voulons faire entendre une autre jeunesse, celle capable de s’émanciper, de s’unir et de réfléchir aux Comores 
de demain. Celle qui demande à être informée des prises de décision sur la construction et l’avenir du pays, et à y 
contribuer activement. Parce que les décisions d’aujourd’hui nous impacteront demain, l’Etat se doit de nous tenir 
informés de toutes ses mesures et d’entendre notre avis. Où que l’on se trouve, même hors des Comores, il est possible 
de se battre pour que notre voix soit entendue : c’est pourquoi nous appelons les jeunes Comoriens qui résident à 
l’étranger à se joindre à nous, en attendant leur retour au pays.

<p style="text-align:justify" ><br />Ngo’shawo a été parmi les initiateurs du Conseil National de la Jeunesse (CNJ) et se bat pour sa ma mise en place 
ainsi qu’un Parlement de la Jeunesse, qui fera entendre la voix des jeunes au niveau de l’Etat. 
                
